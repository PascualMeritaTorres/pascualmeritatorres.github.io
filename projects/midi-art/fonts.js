// Letter width (columns)
const LETTER_WIDTH = 5;
const SPACE_WIDTH = 3;
const LETTER_HEIGHT = 7;
const LETTER_GAP = 1;

/**
 * Convert a single letter to note data
 * @param {string} letter - Single character
 * @param {number} startColumn - Starting column position
 * @param {number} basePitch - Base MIDI pitch (bottom of letter)
 * @returns {Array} Array of note objects
 */
function letterToNotes(letter, startColumn, basePitch = 60) {
  const upperLetter = letter.toUpperCase();
  const grid = PIXEL_FONTS[upperLetter];

  if (!grid) {
    return [];
  }

  const notes = [];

  // Grid is stored top-to-bottom, but we want bottom row to be basePitch
  grid.forEach((row, rowIndex) => {
    const pitch = basePitch + (LETTER_HEIGHT - 1 - rowIndex);
    row.forEach((pixel, colIndex) => {
      if (pixel === 1) {
        notes.push({
          pitch: pitch,
          startTime: startColumn + colIndex,
          duration: 1,
        });
      }
    });
  });

  return notes;
}

/**
 * Convert text string to array of notes
 * @param {string} text - Input text
 * @param {number} basePitch - Base MIDI pitch (default: 60 = C4)
 * @returns {Array} Array of note objects with id, pitch, startTime, duration
 */
function textToNotes(text, basePitch = 60) {
  const notes = [];
  let currentColumn = 0;
  let noteId = 0;

  for (const char of text) {
    const upperChar = char.toUpperCase();

    if (upperChar === " ") {
      currentColumn += SPACE_WIDTH + LETTER_GAP;
    } else if (PIXEL_FONTS[upperChar]) {
      const letterNotes = letterToNotes(upperChar, currentColumn, basePitch);
      letterNotes.forEach((note) => {
        notes.push({
          id: `note-${noteId++}`,
          ...note,
        });
      });
      currentColumn += LETTER_WIDTH + LETTER_GAP;
    }
  }

  return notes;
}

/**
 * Get the total width (in columns) for a text string
 * @param {string} text - Input text
 * @returns {number} Total columns
 */
function getTextWidth(text) {
  let width = 0;

  for (const char of text) {
    const upperChar = char.toUpperCase();
    if (upperChar === " ") {
      width += SPACE_WIDTH + LETTER_GAP;
    } else if (PIXEL_FONTS[upperChar]) {
      width += LETTER_WIDTH + LETTER_GAP;
    }
  }

  // Remove trailing gap
  if (width > 0) {
    width -= LETTER_GAP;
  }

  return width;
}

/**
 * Validate text input
 * @param {string} text - Input text
 * @returns {{valid: boolean, error: string|null}}
 */
function validateText(text) {
  if (!text || text.trim().length === 0) {
    return { valid: false, error: "Please enter some text" };
  }

  if (text.length > 30) {
    return { valid: false, error: "Text must be 30 characters or less" };
  }

  const words = text.trim().split(/\s+/);
  if (words.length > 3) {
    return { valid: false, error: "Maximum 3 words allowed" };
  }

  const invalidChars = text.match(/[^a-zA-Z\s]/g);
  if (invalidChars) {
    const unique = [...new Set(invalidChars)].join(", ");
    return { valid: false, error: `Only letters A-Z allowed. Invalid: ${unique}` };
  }

  return { valid: true, error: null };
}
